# Boolean Algebra Operators

# AND
# OR
# NOT


# AND
if (10 > 20) and (20 > 10):
    print("TRUE")
else:
    print("FALSE")

# OR 
if (10 > 20) or (20 > 10):
    print("TRUE")
else:
    print("FALSE")

# NOT
if not (10 > 20):
    print("TRUE")
else:
    print("FALSE")

# NAND
if not((10 > 20) and (20 > 10)):
    print("TRUE")
else:
    print("FALSE")
